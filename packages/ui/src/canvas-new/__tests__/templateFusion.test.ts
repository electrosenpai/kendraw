import { describe, expect, it } from 'vitest';
import type { Atom, AtomId, Bond, BondId, Command } from '@kendraw/scene';
import {
  RING_TEMPLATE_SMILES,
  fuseTemplate,
  type FuseEndpointResponse,
} from '../templateFusion';

function makeAtom(id: string, x: number, y: number, element = 6): Atom {
  return {
    id: id as AtomId,
    x,
    y,
    element,
    charge: 0,
    radicalCount: 0,
    lonePairs: 0,
  };
}

function makeBond(id: string, from: string, to: string): Bond {
  return {
    id: id as BondId,
    fromAtomId: from as AtomId,
    toAtomId: to as AtomId,
    order: 1,
    style: 'single',
  };
}

const ETHANE_ATOMS: Atom[] = [makeAtom('a1', 0, 0), makeAtom('a2', 1, 0)];
const ETHANE_BONDS: Bond[] = [makeBond('b1', 'a1', 'a2')];

